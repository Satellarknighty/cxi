public enum AvailableCommand {
    /**
     * Enum for hit (drawing a Card).
     */
    HIT,
    /**
     * Enum for stay.
     */
    STAY,
    /**
     * Enum to return a Card on your Hand to the Deck (for now).
     */
    CHEAT,
    /**
     * Enum to shuffle the Deck.
     */
    SHUFFLE,
    /**
     * Enum to look at the top Card of the Deck.
     */
    PEEK,
    /**
     * Enum to quit the game.
     */
    QUIT,
    /**
     * Enum to continue playing the game.
     */
    YES,
    /**
     * Enum for invalid inputs.
     */
    UNKNOWN
}
